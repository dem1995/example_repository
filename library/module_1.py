class Adder:
    """A simple class to add two numbers."""

    def add(self, a: float, b: float) -> float:
        """
        Add two numbers.

        :param a: The first number.
        :param b: The second number.
        :type a: float
        :type b: float
        :return: The sum of the two numbers.
        :rtype: float
        """
        return a + b
